// create multiplication tables by using for loop


for (let num1=1; num1 <= 20; num1++) {
    console.log(`--------------------------${num1} Tabels----------------------------`);
    for (let num2 = 1; num2 <= 10 ; num2++) {
        console.log(`${num2} x ${num1} = ${num1*num2}`);
        
    }
    console.log(`---------------------------------------------------------------`);
    
}